// yarn jest test/errors/04_model/e227_missing_from_field_in_model_filter.test.ts
jest.setTimeout(30000);
test('testName', () => {
  expect.assertions(3);

  return ApStruct.rebuildStruct({
    dir: 'test/errors/04_model/e227',
    weekStart: api.ProjectWeekStartEnum.Monday,
    connection: api.ProjectConnectionEnum.BigQuery,
    bqProject: 'flow-1202',
    projectId: 'unkProjectId',
    structId: 'unkStructId'
  }).then((struct: interfaces.Struct) => {
    const desiredError0Lines: interfaces.ErrorLine[] = [
      { line: 6, name: 'e227m.model', path: 'e227m.model' }
    ];

    expect(struct.errors[0].lines).toEqual(
      expect.arrayContaining(desiredError0Lines)
    );
    expect(struct.errors[0].title).toEqual(
      `missing from_field in model filter`
    );
    expect(struct.errors[0].message).toEqual(
      `parameter 'from_field:' must be set for filters with 'result: from_field`
    );
  });
});
