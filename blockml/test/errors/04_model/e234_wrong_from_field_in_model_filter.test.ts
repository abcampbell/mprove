// yarn jest test/errors/04_model/e234_wrong_from_field_in_model_filter.test.ts
jest.setTimeout(30000);
test('testName', () => {
  expect.assertions(3);

  return ApStruct.rebuildStruct({
    dir: 'test/errors/04_model/e234',
    weekStart: api.ProjectWeekStartEnum.Monday,
    bqProject: 'flow-1202',
    projectId: 'unkProjectId',
    structId: 'unkStructId'
  }).then((struct: interfaces.Struct) => {
    const desiredError0Lines: interfaces.ErrorLine[] = [
      { line: 8, name: 'e234m.model', path: 'e234m.model' }
    ];

    expect(struct.errors[0].lines).toEqual(
      expect.arrayContaining(desiredError0Lines)
    );
    expect(struct.errors[0].title).toEqual(`wrong from_field in model filter`);
    expect(struct.errors[0].message).toEqual(
      `model filter "from_field:" value must be specified in ` +
        `a form of "join_alias.view_field_name" or "model_field_name"`
    );
  });
});
