// yarn jest test/unit/11_report/e87_wrong_select_field.test.ts
jest.setTimeout(30000);
test('testName', () => {
  expect.assertions(3);

  return ApStruct.rebuildStruct({
    dir: 'test/unit/11_report/e87',
    weekStart: api.ProjectWeekStartEnum.Monday,
    bqProject: 'flow-1202',
    projectId: 'unkProjectId',
    structId: 'unkStructId'
  }).then((struct: interfaces.Struct) => {
    const desiredError0Lines: interfaces.ErrorLine[] = [
      { line: 6, name: 'e87d.dashboard', path: 'e87d.dashboard' }
    ];

    expect(struct.errors[0].lines).toEqual(
      expect.arrayContaining(desiredError0Lines)
    );
    expect(struct.errors[0].title).toEqual(`wrong select field`);
    expect(struct.errors[0].message).toEqual(
      `found element "- mf.unk" references missing or not valid field ` +
        `"unk" in fields section of model "e87m"`
    );
  });
});
